SELECT u.full_name, COUNT(r.review_id) as comment_count
FROM Users u
JOIN Reviews r ON u.user_id = r.user_id
GROUP BY u.user_id, u.full_name
ORDER BY comment_count DESC
LIMIT 1;
--кол-во жилья в городах 
SELECT c.city_name, COUNT(h.hous_id) as housing_count
FROM Cities c
LEFT JOIN Housing h ON c.city_id = h.city_id
GROUP BY c.city_id, c.city_name
ORDER BY housing_count DESC;

