CREATE TABLE users (
    user_id SERIAL PRIMARY KEY,
    full_name VARCHAR(255),
    phone VARCHAR(50),
    email VARCHAR(255)
);

CREATE TABLE owners (
    owner_id SERIAL PRIMARY KEY,
    full_name VARCHAR(255),
    phone VARCHAR(50),
    email VARCHAR(255)
);

CREATE TABLE housing (
    housing_id SERIAL PRIMARY KEY,
    owner_id INT REFERENCES owners(owner_id),
    type_housing VARCHAR(100),
    price NUMERIC(10,2),
    address VARCHAR(255)
);

CREATE TABLE amenities (
    amenities_id SERIAL PRIMARY KEY,
    name VARCHAR(100)
);

CREATE TABLE housing_amenities (
    hous_amen_id SERIAL PRIMARY KEY,
    housing_id INT REFERENCES housing(housing_id),
    amenities_id INT REFERENCES amenities(amenities_id)
);

CREATE TABLE statuses (
    status_id SERIAL PRIMARY KEY,
    status VARCHAR(50)
);

CREATE TABLE reservations (
    reservation_id SERIAL PRIMARY KEY,
    date_in DATE,
    date_out DATE,
    reservation_date DATE,
    total_price NUMERIC(10,2),
    status_id INT REFERENCES statuses(status_id),
    housing_id INT REFERENCES housing(housing_id),
    user_id INT REFERENCES users(user_id)
);

CREATE TABLE reviews (
    review_id SERIAL PRIMARY KEY,
    housing_id INT REFERENCES housing(housing_id),
    user_id INT REFERENCES users(user_id),
    comment TEXT,
    rating INT
);

