CREATE TABLE Users(
    user_id SERIAL PRIMARY KEY,
    full_name VARCHAR(255) NOT NULL,
    email VARCHAR(255) UNIQUE,
    phone VARCHAR(20) UNIQUE  
);

CREATE TABLE Cities(
    city_id SERIAL PRIMARY KEY,
    city_name VARCHAR(255)
);

CREATE TABLE Aminities(
    am_id SERIAL PRIMARY KEY,
    am_name VARCHAR(255)
);

CREATE TABLE Housing(
    hous_id SERIAL PRIMARY KEY,
    user_id INT REFERENCES Users(user_id),
    type_hous VARCHAR(255),
    per_night NUMERIC(10,2),
    per_mouth NUMERIC(10,2),
    hous_address VARCHAR(255),
    city_id INT REFERENCES Cities(city_id) 
);
CREATE TABLE Reviews(
    review_id SERIAL PRIMARY KEY,
    user_id INT REFERENCES Users(user_id),
    hous_id INT REFERENCES Housing(hous_id),
    rating INT,
    rev_comment TEXT
);
CREATE TABLE Reservations(
    reserv_id SERIAL PRIMARY KEY,
    date_in DATE,
    date_out DATE,
    reserv_date DATE,
    total_price NUMERIC(10,2),
    user_id INT REFERENCES Users(user_id),
    hous_id INT REFERENCES Housing(hous_id)
);


